CREATE VIEW [dbo].[HFMBANKBALANCE]
AS
SELECT     BALANCE.ACCOUNTID AS [Bank  account], BALANCE.[Amount in bank currency], BALANCE.[Amount in company currency], BALANCE.CURRENCYCODE AS [Bank account currency], dbo.LEDGER.ACCOUNTINGCURRENCY AS [Legal entity currency], 
                  BALANCE.[Legal entity]
FROM        (SELECT     ACCOUNTID, SUM(AMOUNTCUR) + SUM(AMOUNTCORRECT) AS [Amount in bank currency], SUM(AMOUNTMST) AS [Amount in company currency], CURRENCYCODE, DATAAREAID AS [Legal entity]
                   FROM        dbo.BANKACCOUNTTRANS
                   GROUP BY ACCOUNTID, CURRENCYCODE, DATAAREAID) AS BALANCE INNER JOIN
                  dbo.LEDGER ON BALANCE.[Legal entity] = dbo.LEDGER.NAME

GO

